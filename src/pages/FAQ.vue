              <a
                href="https://bpifrance-creation.fr/encyclopedie/structures-juridiques/entreprendre-seul/eirl-entrepreneur-individuel-a-responsabilite"
              >EIRL</a> (entrepreneur individuel à responsabilité limitée). Cette option lui permet de constituer un patrimoine affecté à son activitéprofessionnelle distinct de son patrimoine personnel.
            </li>
