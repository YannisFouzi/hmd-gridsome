            <h5 class="title is-5">En choisissant l’entreprise individuelle</h5>
            <li>
              Vous ne formerez, avec votre entreprise, qu’une seule et même personne. Vous aurez donc une grande liberté d’action, serez seul maître à bord et n’aurez de comptes à rendre à personne. En effet, la notion d’abus de bien social n’existe pas dans l’entreprise individuelle.
              <br />En contrepartie, vos patrimoines professionnel et personnel seront juridiquement confondus. Vous serez en principe responsable des dettes de votre entreprise sur l’ensemble de vos biens, y compris sur ceux acquis avec votre conjoint, si vous êtes marié sous le régime légal de la communauté réduite aux acquêts.
            </li>
            <li>
              Heureusement la loi protège de droit votre résidence principale en la mettant à l’abri de vos créanciers professionnels. Elle vous permet par ailleurs de protéger vos autres biens fonciers en procédant à une
              <a
                href="https://bpifrance-creation.fr/encyclopedie/structures-juridiques/entreprendre-seul/declaration-dinsaisissabilite"
              >déclaration d’insaisissabilité</a> devant notaire
            </li>

