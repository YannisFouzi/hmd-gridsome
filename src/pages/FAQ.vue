              <a
                href="https://bpifrance-creation.fr/encyclopedie/fiscalite-lentreprise/generalites/is-impot-societe"
              >impôt sur les sociétés (IS)</a>, soit de plein droit, soit sur option.
            </li>
            <li>
              <a
                href="https://bpifrance-creation.fr/moment-de-vie/a-quel-regime-social-serai-je-rattache"
              >Votre statut social dépendra de la structure juridique choisie</a>. Si vous êtes gérant majoritaire de SARL ou associé unique d’une EURL, vous serez rattaché au régime des indépendants.
              <br />Dans les autres cas, vous dépendrez du régime général de la sécurité sociale (régime des assimilés-salariés).
            </li>
            <li>La création de votre société donnera lieu à des formalités complémentaires : rédaction et enregistrement des statuts auprès du service des impôts, nomination du ou des dirigeants, parution d’une annonce dans un journal d’annonces légales.</li>
