              <a
                href="https://bpifrance-creation.fr/encyclopedie/structures-juridiques/entreprendre-a-plusieurs/snc-societe-nom-collectif"
              >SNC</a> par exemple), les associés sont solidairement et indéfiniment responsables avec la société. Consultez le
              <a
                href="https://bpifrance-creation.fr/encyclopedie/structures-juridiques/choix-du-statut-generalites/structures-juridiques-comparaison"
              >tableau comparant les différents types de sociétés</a>.
            </li>
            <li>Si vous utilisez les biens de votre société à des fins personnelles, vous pourrez être poursuivi au titre de l’abus de biens sociaux.</li>
            <br />
            <li>S’agissant d’une “nouvelle personne”, votre société aura un nom (dénomination sociale), un domicile (siège social) et devra disposer d’un minimum d’apports constituant son patrimoine initial pour faire face à ses premiers investissements et premières dépenses (capital social).</li>
            <li>
              En tant que dirigeant désigné pour représenter la société vis-à-vis des tiers, vous n’agirez pas “pour votre compte”, mais “au nom et pour le compte” d’une autre personne. Vous devrez donc respecter un certain formalisme lorsque vous devrez prendre des décisions importantes.
              <br />De même, vous devrez périodiquement rendre des comptes à vos coassociés sur votre gestion.
            </li>
            <li>
