              <a
                href="https://bpifrance-creation.fr/encyclopedie/trouver-proteger-tester-son-idee/proteger-son-idee/nom-lentreprise"
              >nom commercial.</a>
              <br />Exemple : Entreprise Marion Durand, Atelier des étoiles.
            </li>

