            <br />Vous donnerez naissance à une nouvelle personne (personne morale), distincte de vous juridiquement. Par conséquent :
            <li>Votre entreprise disposera de son propre patrimoine. En cas de difficultés (et si vous n’avez pas commis de faute de gestion grave en tant que dirigeant de droit ou de fait), vos biens personnels seront à l’abri de l’action des créanciers de l’entreprise.</li>
            <li>
